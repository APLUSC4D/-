#!/bin/bash
# ==================================================
# 本地素材管理器 - 桌面端软件打包工具 (Mac OS)
# ==================================================

# 获取当前路径
cd "$(dirname "$0")"
clear

echo "=================================================="
echo "        正在为您生成专属的 Mac 桌面版软件 🚀"
echo "=================================================="
echo ""
echo "💡 为什么需要打包？"
echo "   打包后，我们将把代码、网页引擎和运行环境融为一体，"
echo "   生成一个漂亮的 Mac 「.dmg」安装程序。"
echo "   之后，您就不需要装任何引擎，直接双击运行应用就能用了！"
echo "--------------------------------------------------"
echo ""

# 1. 检查 Node.js
echo "[1/4] 正在检查打包环境..."
if ! command -v node &> /dev/null
then
    echo ""
    echo "❌【错误】您的苹果电脑尚未安装 Node.js"
    echo "请用浏览器打开下面链接下载安装包（傻瓜式点下一步即可）："
    echo "👉 官方快速下载: https://nodejs.org/dist/v20.11.1/node-v20.11.1.pkg"
    echo ""
    exit 1
fi

echo "✅ 环境正确！开始安装依赖库..."
echo ""

# 2. 安装库
echo "[2/4] 加载库依赖 (正在切换国内高速下载，需15秒)..."
npm config set registry https://registry.npmmirror.com
npm install --no-audit --no-fund
echo "✅ 依赖加载完成！"
echo ""

# 3. 运行打包
echo "[3/4] 正在为您打包 Mac dmg 安装包..."
echo "💡 打包时间大约需要 40-80 秒，请耐心等待本窗口，切勿关闭哦！"
echo ""
npm run dist:mac

# 4. 判断结果
if [ $? -eq 0 ]; then
    echo ""
    echo "=================================================="
    echo "🎉🎉 恭喜！！！Mac 桌面版软件打包成功！ ⭐⭐⭐⭐⭐"
    echo "=================================================="
    echo "📍 我们已在您当前的文件夹下生成了 Mac「.app」和安装包。"
    echo "   请进入 【dist-desktop】目录查看"
    echo "   双击里面的 「本地素材管理器.dmg」安装到系统应用中即可！"
    echo "=================================================="
else
    echo "❌ 打包遇到了错误，请检查是否有权限问题或被安全拦截。"
fi

echo ""
read -p "按回车键退出..."
