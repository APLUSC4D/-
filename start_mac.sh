#!/bin/bash
# ==========================================
# Local Media Manager - 一键启动脚本 (Mac)
# ==========================================

# 获取脚本所在根目录
cd "$(dirname "$0")"

# 终端窗口清理
clear

echo "=================================================="
echo "        欢迎使用【本地素材管理器】一键启动器 (Mac)"
echo "=================================================="
echo ""

echo "[1/3] 正在检查本地环境..."

if ! command -v node &> /dev/null
then
    echo ""
    echo "--------------------------------------------------"
    echo "❌【提示】你的电脑上还没有正确安装 Node.js！"
    echo ""
    echo "这是一个免费的网格运行引擎，点击这里下载安装："
    echo "🚀 推荐下载链接: https://nodejs.org/dist/v20.11.1/node-v20.11.1.pkg"
    echo ""
    echo "安装完成后，重新运行此程序即可。"
    echo "--------------------------------------------------"
    echo ""
    read -p "按键盘上的 [回车键] 退出..."
    exit 1
fi

echo "✅ 环境检查通过！检测到 Node.js 版本: $(node -v)"
echo ""

echo "[2/3] 正在为您安装库文件 (大约需要10-20秒)..."
echo "💡 小贴士：为了让你下载更快，我们将自动切换为国内高速下载通道 (淘宝镜像源)"
echo ""

npm config set registry https://registry.npmmirror.com
npm install --no-audit --no-fund

echo ""
echo "[3/3] 正在为您打开本地素材库..."
echo "🚀 服务启动中，请不要关闭目前的终端小窗口。"
echo ""
echo "--------------------------------------------------"
echo "🎉 启动成功！正在自动为您拉起默认浏览器..."
echo "💡 小贴士: 如果浏览器刚打开时网页显示空白或连接拒绝,"
echo "   这是因为服务器在“热身”，请等待5-10秒后按下 Command + R 刷新即可！"
echo "--------------------------------------------------"
echo ""

# 后台打开浏览器
open "http://localhost:3000"

# 启动服务器
npm run dev
